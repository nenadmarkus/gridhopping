#ifndef ABS
#define ABS(x) ((x)<0?(-(x)):(x))
#endif

void interpolate_vertex(real_t borderval, real_t vert1[], real_t val1, real_t vert2[], real_t val2, real_t vert[])
{
	/*
		Linearly interpolate the position where an isosurface cuts
		an edge between two vertices, each with their own scalar value
	*/

	if (ABS(borderval-val1)<0.00001f || ABS(val1-val2)<0.00001f)
	{
		vert[0] = vert1[0];
		vert[1] = vert1[1];
		vert[2] = vert1[2];

		return;
	}

	if (ABS(borderval-val2) < 0.00001f)
	{
		vert[0] = vert2[0];
		vert[1] = vert2[1];
		vert[2] = vert2[2];

		return;
	}

	real_t a = (borderval - val1) / (val2 - val1);

	vert[0] = vert1[0] + a*(vert2[0] - vert1[0]);
	vert[1] = vert1[1] + a*(vert2[1] - vert1[1]);
	vert[2] = vert1[2] + a*(vert2[2] - vert1[2]);
}



