/*
	- surface crawling
	- quadratic complexity
*/

#include <stdint.h>
#include <stack>
#include <tuple>
#include <unordered_map>
using namespace std;

int surface_crawling(real_t (*eval_sdb)(real_t x, real_t y, real_t z), real_t seeds[], int nseeds, real_t side, int n, void (*emit_triangle)(real_t v1[], real_t v2[], real_t v3[]))
{
	real_t h = side/n;
	int ntriang = 0;

	/*
		prepare stack and map
	*/
	stack<tuple<int, int, int>> STACK;
	int seedind;
	for (seedind=0; seedind<nseeds; ++seedind)
	{
		real_t x=seeds[3*seedind+0], y=seeds[3*seedind+1], z=seeds[3*seedind+2];

		// grid cell from (x, y, z)
		int i = (int)( (x + side/2.0f  - h/2.0f)/h );
		int j = (int)( (y + side/2.0f  - h/2.0f)/h );
		int k = (int)( (z + side/2.0f  - h/2.0f)/h );

		//
		STACK.push( tuple<int, int, int>(i, j, k) );
	}

	unordered_map<uint64_t, bool> HASHTABLE;

	/*
		main loop
	*/

	while (
		!STACK.empty()
	)
	{
		//
		tuple<int, int, int> ijk = STACK.top();
		STACK.pop();
		
		//
		int i=get<0>(ijk), j=get<1>(ijk), k=get<2>(ijk);
		uint64_t key = i + j*n + k*n*n;

		if (HASHTABLE.count(key) == 1)
			// cell already visited
			continue;
		else
			HASHTABLE[key] = true;

		real_t xm = i*h-side/2.0, xp = (i+1)*h-side/2.0;
		real_t ym = j*h-side/2.0, yp = (j+1)*h-side/2.0;
		real_t zm = k*h-side/2.0, zp = (k+1)*h-side/2.0;

		real_t cuboid[] = {
			xm, ym, zm,
			xp, ym, zm,
			xp, yp, zm,
			xm, yp, zm,
			xm, ym, zp,
			xp, ym, zp,
			xp, yp, zp,
			xm, yp, zp
		};

		real_t vals[8] = {
			eval_sdb(cuboid[3*0 + 0], cuboid[3*0 + 1], cuboid[3*0 + 2]),
			eval_sdb(cuboid[3*1 + 0], cuboid[3*1 + 1], cuboid[3*1 + 2]),
			eval_sdb(cuboid[3*2 + 0], cuboid[3*2 + 1], cuboid[3*2 + 2]),
			eval_sdb(cuboid[3*3 + 0], cuboid[3*3 + 1], cuboid[3*3 + 2]),
			eval_sdb(cuboid[3*4 + 0], cuboid[3*4 + 1], cuboid[3*4 + 2]),
			eval_sdb(cuboid[3*5 + 0], cuboid[3*5 + 1], cuboid[3*5 + 2]),
			eval_sdb(cuboid[3*6 + 0], cuboid[3*6 + 1], cuboid[3*6 + 2]),
			eval_sdb(cuboid[3*7 + 0], cuboid[3*7 + 1], cuboid[3*7 + 2])
		};

		int t = polygonize_cell(cuboid, vals, 0.0f, emit_triangle);

		if (t > 0)
		{
			STACK.push( tuple<int, int, int>(i+1, j, k) );
			STACK.push( tuple<int, int, int>(i, j+1, k) );
			STACK.push( tuple<int, int, int>(i, j, k+1) );
			STACK.push( tuple<int, int, int>(i-1, j, k) );
			STACK.push( tuple<int, int, int>(i, j-1, k) );
			STACK.push( tuple<int, int, int>(i, j, k-1) );
		}

		ntriang += t;
	}

	/*
		we're done
	*/

	return ntriang;
}